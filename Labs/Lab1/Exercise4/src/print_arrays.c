/**
 * @brief       Function takes two char arrays and prints both of them.
 *
 * @param base  Base array given by user.
 * @param mod   Modified array, with random lowercase and uppercase letters.
 */
void PrintArrays(char* base, char* mod) {
  printf("Base array: %s", base);
  printf("Modified array: %s", mod);
}